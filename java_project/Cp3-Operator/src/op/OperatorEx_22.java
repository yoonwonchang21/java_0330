package op;

public class OperatorEx_22 {
	public static void main(String[] args) {
		
	if(10 == 10.0f) {		//10이 10.0f와 같다면
		System.out.println("10과 10.0f는 같다.");
		}
		if('0' != 0) {		//'0'의 문자코드값이 0이 아니라면
		System.out.println("'0'과 0은 같지 않다.");
		}
		if('A' == 65) {		//'A'의 문자코드값이 65와 같다면
		System.out.println("\"A\"는 65와 같다.");
		}
		int num = 5;		//변수 num에 5를 저장
		if( num > 0 && num < 9) {	//num의 값이 0보다 크면서 9보다 작다면
		System.out.println("5는 0보다 크고, 9보다는 작다.");
		}
		
		
	}
}
