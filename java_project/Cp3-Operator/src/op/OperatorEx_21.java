package op;

public class OperatorEx_21 {
	public static void main(String[] args) {
		
		System.out.println(-10%8);
		//-10을 8로 나눈 나머지
		System.out.println(10%-8);
		//10을 -8로 나눈 나머지
		System.out.println(-10%-8);
		//-10을 -8로 나눈 나머지
		//값의 부호는 피제수의 부호에 영향받는다
		//피제수가 음수면 결과 값도 음수, 양수면 결과 값도 양수
	}
}
