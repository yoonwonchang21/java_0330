package op;

public class OperatorEx_19 {
	public static void main(String[] args) {
		
		int share = 10 / 8;		//int타입 변수 share를 선언하고 10/8한 값을 저장
		int remain = 10 % 8;	//int타입 변수 remain를 선언하고 10%8한 값을 저장
		System.out.println("10을 8로 나누면, ");
		System.out.println("몫은 "+share+"이고, 나머지는 "+remain+"입니다.");
		//몫은 1, 나머지는 2 출력(int타입)
	
	}
}
