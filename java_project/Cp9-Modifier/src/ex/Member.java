package ex;

public class Member {

	private String name;
	private String id;
	
	//디폴트 생성자 자동생성됨

	
	
	
	//getter & setter 메서드
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	

	
	
}
