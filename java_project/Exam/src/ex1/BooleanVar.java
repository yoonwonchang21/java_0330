package ex1;

public class BooleanVar {

	public static void main(String[] args) {
		
		boolean b1 = true;
		boolean b2 = false;
		
		int num = 100;
		
		System.out.println(b1);
		System.out.println(b2);
		System.out.println("----------------");
		System.out.println(3<4);
		System.out.println(3>4);
		System.out.println("----------------");
		System.out.println(num>20);
		System.out.println(num<20);
		
		int n = 100;
		System.out.println(n);
		
		
	}
	
	
}
